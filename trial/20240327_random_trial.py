def dijkstra_with_specific_nodes_and_target_distance(graph, start, target_distance, specific_nodes, threshold):
    distances = {node: float('inf') for node in graph}
    distances[start] = 0
    visited = set()
    specific_nodes_visited = set()
    queue = [(0, start, [])]

    while queue:
        current_distance, current_node, path = heapq.heappop(queue)

        if current_node in visited:
            continue

        visited.add(current_node)
        path = path + [current_node]

        if current_node in specific_nodes:
            specific_nodes_visited.add(current_node)

        # Check if all specific nodes have been visited
        if specific_nodes_visited == set(specific_nodes) and abs(current_distance - target_distance) <= threshold:
            return path, current_distance

        for neighbor, weight in graph[current_node]:
            if neighbor not in visited:
                new_distance = current_distance + weight
                if new_distance < distances[neighbor]:
                    distances[neighbor] = new_distance
                    heapq.heappush(queue, (new_distance, neighbor, path))

    return [], float('inf')

